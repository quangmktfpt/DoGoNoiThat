    @Override
    public void login() {
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());

        // 1. Validate
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß t√™n ƒëƒÉng nh·∫≠p v√† m·∫≠t kh·∫©u!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Ki·ªÉm tra t√†i kho·∫£n theo t·ª´ng b∆∞·ªõc
        poly.dao.UserDAO userDAO = new poly.dao.impl.UserDAOImpl();
        
        // 2.1. Ki·ªÉm tra username c√≥ t·ªìn t·∫°i kh√¥ng
        poly.entity.User existingUser = userDAO.selectByUsername(username);
        if (existingUser == null) {
            JOptionPane.showMessageDialog(this, "‚ùå T√™n ƒëƒÉng nh·∫≠p kh√¥ng t·ªìn t·∫°i!\n\nVui l√≤ng ki·ªÉm tra l·∫°i t√™n ƒëƒÉng nh·∫≠p ho·∫∑c ƒëƒÉng k√Ω t√†i kho·∫£n m·ªõi.", 
                "T√™n ƒëƒÉng nh·∫≠p kh√¥ng ƒë√∫ng", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 2.2. Ki·ªÉm tra t√†i kho·∫£n c√≥ b·ªã kh√≥a kh√¥ng
        if (existingUser.getIsActive() == null || !existingUser.getIsActive()) {
            JOptionPane.showMessageDialog(this, "üö´ T√†i kho·∫£n ƒë√£ b·ªã kh√≥a!\n\nT√†i kho·∫£n c·ªßa b·∫°n ƒë√£ b·ªã v√¥ hi·ªáu h√≥a do c√≥ h√†nh vi x·∫•u, li√™n h·ªá admin ƒë·ªÉ ƒë∆∞·ª£c h·ªó tr·ª£.", 
                "T√†i kho·∫£n b·ªã kh√≥a", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 2.3. Ki·ªÉm tra m·∫≠t kh·∫©u c√≥ ƒë√∫ng kh√¥ng
        if (!password.equals(existingUser.getPasswordHash())) {
            JOptionPane.showMessageDialog(this, "üîê M·∫≠t kh·∫©u kh√¥ng ƒë√∫ng!\n\nVui l√≤ng ki·ªÉm tra l·∫°i m·∫≠t kh·∫©u ho·∫∑c s·ª≠ d·ª•ng ch·ª©c nƒÉng qu√™n m·∫≠t kh·∫©u.", 
                "M·∫≠t kh·∫©u kh√¥ng ƒë√∫ng", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. ƒêƒÉng nh·∫≠p th√†nh c√¥ng
        poly.entity.User user = existingUser;
        
        // 4. L∆∞u th√¥ng tin user ƒëƒÉng nh·∫≠p v√†o ti·ªán √≠ch to√†n c·ª•c
        poly.util.CurrentUserUtil.setCurrentUserId(user.getUserId());
        poly.util.CurrentUserUtil.setCurrentUsername(user.getUsername());

        // 5. Chuy·ªÉn m√†n h√¨nh theo quy·ªÅn
        String roleMsg = user.getRole() != null && user.getRole() ? "Admin" : "Kh√°ch h√†ng";
        JOptionPane.showMessageDialog(this, "‚úÖ ƒêƒÉng nh·∫≠p th√†nh c√¥ng!\n\nXin ch√†o " + user.getUsername() + " (" + roleMsg + ")", 
            "ƒêƒÉng nh·∫≠p th√†nh c√¥ng", JOptionPane.INFORMATION_MESSAGE);

        this.dispose();
        if (user.getRole() != null && user.getRole()) {
            // Admin
            new poly.ui.AdminJFrame().setVisible(true);
        } else {
            // Kh√°ch h√†ng
            new poly.ui.KhachJFrame().setVisible(true);
        }
    }

