                System.out.println("DEBUG: Checking product " + productId + " for user " + currentUserId);
                
                // Ki·ªÉm tra xem user ƒë√£ ƒë√°nh gi√° s·∫£n ph·∫©m n√†y trong ƒë∆°n h√†ng n√†y ch∆∞a
