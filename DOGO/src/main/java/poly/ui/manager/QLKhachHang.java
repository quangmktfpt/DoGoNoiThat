    @Override
    public String validateCustomerInfo(User user) {
        StringBuilder errorMessage = new StringBuilder();
        boolean hasError = false;
        
        // Ki·ªÉm tra Username
        if (user.getUsername() == null || user.getUsername().trim().isEmpty()) {
            errorMessage.append("‚ùå T√™n ƒëƒÉng nh·∫≠p kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!\n");
            hasError = true;
        } else if (user.getUsername().length() < 3) {
            errorMessage.append("‚ùå T√™n ƒëƒÉng nh·∫≠p ph·∫£i c√≥ √≠t nh·∫•t 3 k√Ω t·ª±!\n");
            hasError = true;
        } else if (!user.getUsername().matches("^[a-zA-Z0-9_]+$")) {
            errorMessage.append("‚ùå T√™n ƒëƒÉng nh·∫≠p ch·ªâ ƒë∆∞·ª£c ch·ª©a ch·ªØ c√°i, s·ªë v√† d·∫•u g·∫°ch d∆∞·ªõi!\n");
            hasError = true;
        }
        
        // Ki·ªÉm tra Email
        if (user.getEmail() == null || user.getEmail().trim().isEmpty()) {
            errorMessage.append("‚ùå Email kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!\n");
            hasError = true;
        } else {
            // Validate email format
            String emailPattern = "^[A-Za-z0-9+_.-]+@(.+)$";
            if (!Pattern.matches(emailPattern, user.getEmail())) {
                errorMessage.append("‚ùå Email kh√¥ng ƒë√∫ng ƒë·ªãnh d·∫°ng!\n");
                hasError = true;
            }
        }
        
        // Ki·ªÉm tra m·∫≠t kh·∫©u (ch·ªâ khi t·∫°o m·ªõi)
        if (user.getPasswordHash() != null && !user.getPasswordHash().trim().isEmpty()) {
            if (user.getPasswordHash().length() < 6) {
                errorMessage.append("‚ùå M·∫≠t kh·∫©u ph·∫£i c√≥ √≠t nh·∫•t 6 k√Ω t·ª±!\n");
                hasError = true;
            }
        }
        
        // Ki·ªÉm tra h·ªç t√™n
        if (user.getFullName() == null || user.getFullName().trim().isEmpty()) {
            errorMessage.append("‚ùå H·ªç t√™n kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!\n");
            hasError = true;
        } else if (user.getFullName().trim().length() < 2) {
            errorMessage.append("‚ùå H·ªç t√™n ph·∫£i c√≥ √≠t nh·∫•t 2 k√Ω t·ª±!\n");
            hasError = true;
        }
        
        // Ki·ªÉm tra s·ªë ƒëi·ªán tho·∫°i (n·∫øu c√≥ nh·∫≠p)
        if (user.getPhone() != null && !user.getPhone().trim().isEmpty()) {
            // Validate phone format (Vietnamese phone number)
            String phonePattern = "^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])|(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$";
            if (!Pattern.matches(phonePattern, user.getPhone())) {
                errorMessage.append("‚ùå S·ªë ƒëi·ªán tho·∫°i kh√¥ng ƒë√∫ng ƒë·ªãnh d·∫°ng!\n");
                hasError = true;
            }
        }
        
        if (hasError) {
            errorMessage.insert(0, "üìã Vui l√≤ng ki·ªÉm tra v√† s·ª≠a c√°c l·ªói sau:\n\n");
            return errorMessage.toString();
        }
        
        return null; // No validation errors
    }

