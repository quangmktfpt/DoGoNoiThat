    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {
        String keyword = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        if (keyword.isEmpty()) {
            // Nếu từ khóa rỗng, hiển thị tất cả
            fillToTable();
            return;
        }
        
        int foundCount = 0;
        for (Coupon c : couponDAO.selectAll()) {
            if (c.getCouponId().toLowerCase().contains(keyword) ||
                (c.getDescription() != null && c.getDescription().toLowerCase().contains(keyword))) {
                
                // Lấy trạng thái từ database
                String status = c.getStatus() != null ? c.getStatus() : "Hoạt động";
                
                model.addRow(new Object[] {
                    c.getCouponId(),
                    status,
                    c.getDescription(),
                    c.getDiscountType(),
                    c.getDiscountValue(),
                    c.getStartDate(),
                    c.getEndDate(),
                    false
                });
                foundCount++;
            }
        }
        
        // Hiển thị kết quả tìm kiếm
        if (foundCount > 0) {
            JOptionPane.showMessageDialog(this, 
                "Tìm thấy " + foundCount + " kết quả cho từ khóa: '" + keyword + "'", 
                "Kết quả tìm kiếm", 
                JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                "Không tìm thấy kết quả nào cho từ khóa: '" + keyword + "'", 
                "Kết quả tìm kiếm", 
                JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keyword = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        if (keyword.isEmpty()) {
            // Nếu từ khóa rỗng, hiển thị tất cả
            fillToTable();
            return;
        }
        
        int foundCount = 0;
        for (Coupon c : couponDAO.selectAll()) {
            if (c.getCouponId().toLowerCase().contains(keyword) ||
                (c.getDescription() != null && c.getDescription().toLowerCase().contains(keyword))) {
                model.addRow(new Object[] {
                    c.getCouponId(),
                    c.getDescription(),
                    c.getDiscountType(),
                    c.getDiscountValue(),
                    c.getStartDate(),
                    c.getEndDate(),
                    false
                });
                foundCount++;
            }
        }
        
        // Hiển thị kết quả tìm kiếm
        if (foundCount > 0) {
            JOptionPane.showMessageDialog(this, 
                "Tìm thấy " + foundCount + " kết quả cho từ khóa: '" + keyword + "'", 
                "Kết quả tìm kiếm", 
                JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                "Không tìm thấy kết quả nào cho từ khóa: '" + keyword + "'", 
                "Kết quả tìm kiếm", 
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

